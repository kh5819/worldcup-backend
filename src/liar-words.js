export function pickRandomPair(categoryId) {
  const list = LIAR_KEYWORDS[categoryId] || [];
  if (!list.length) return null;
  const pair = list[Math.floor(Math.random() * list.length)];
  // 랜덤 셔플 — 어느 쪽이 시민 키워드인지도 랜덤
  if (Math.random() < 0.5) return [pair[0], pair[1]];
  return [pair[1], pair[0]];
}

export function totalPairsByCategory() {
  const out = {};
  for (const id of Object.keys(LIAR_KEYWORDS)) out[id] = LIAR_KEYWORDS[id].length;
  return out;
}
